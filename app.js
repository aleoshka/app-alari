// Операторы присваивания
let age = 18 + 5;
age += 3; // age = age + 2
age -= 3;
age *= 2;
age /= 2;

age++; // age = age + 1
age--;
console.log(age);

// Операторы сравнения
const vasia = 20;
console.log(age > vasia);
console.log(age >= vasia);
console.log(age < vasia);
console.log(age <= vasia);
console.log(age == vasia);